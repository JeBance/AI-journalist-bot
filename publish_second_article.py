print("📝 Публикация второй статьи на Telegra.ph...")

# Загружаем конфиг
config = json.load(open('/root/git/AI-journalist-bot/telegraph_config.json'))
access_token = config.get('access_token')

# Конвертируем контент
content_nodes = create_telegraph_content(article_content)

# Публикуем
url = "https://api.telegra.ph/createPage"
data = {
    "access_token": access_token,
    "title": "10 ситуаций, где ИИ уже работает: практическое руководство",
    "content": json.dumps(content_nodes),
    "return_content": False
}

response = requests.post(url, json=data)
result = response.json()

if result.get("ok"):
    article_url = result["result"]["url"]
    print(f"✅ Статья опубликована!")
    print(f"📰 URL: {article_url}")
    
    # Создаём анонс
    announcement = f"""📖 <b>Новая статья на Telegra.ph</b>

<b>10 ситуаций, где ИИ уже работает</b>

Практическое руководство по применению нейросетей:

- Написание текстов
- Программирование
- Обработка изображений
- Перевод и обучение
- Анализ данных и не только

<a href="{article_url}">Читать статью →</a>

#ai #технологии #практика #нейросети"""
    
    print("\n📢 Публикация анонса в Telegram...")
    
    config = json.load(open('/root/git/AI-journalist-bot/config.json'))
    token = config['token']
    channel_id = config['channel_id']
    
    tg_url = f'https://api.telegram.org/bot{token}/sendMessage'
    tg_data = {
        'chat_id': channel_id,
        'text': announcement,
        'parse_mode': 'HTML'
    }
    
    tg_result = requests.post(tg_url, json=tg_data).json()
    
    if tg_result.get('ok'):
        print(f"✅ Анонс опубликован! Message ID: {tg_result['result']['message_id']}")
        
        # Записываем в историю
        print("\n📝 Запись в историю...")
        with open('/root/git/AI-journalist/06_history/01_published_posts.md', 'r', encoding='utf-8') as f:
            history = f.read()
        
        new_entry = f"""### [2026-03-25] 10 ситуаций, где ИИ уже работает

- **Категория:** ai_tools
- **Шаблон:** telegra.ph article
- **Ключевые темы:** AI, практическое применение, нейросети, инструменты
- **Источники:** 
  - MIT Technology Review: The AI 100
  - Stanford HAI: AI Index Report 2026
  - Harvard Business Review: How AI Is Used in Business Today
- **Telegra.ph URL:** {article_url}
- **Telegram ID:** {tg_result['result']['message_id']}
- **Статус:** опубликован

---

"""
        
        # Вставляем после последней записи
        history = history.replace('---\n\n## 📊 Статистика', new_entry + '---\n\n## 📊 Статистика')
        
        with open('/root/git/AI-journalist/06_history/01_published_posts.md', 'w', encoding='utf-8') as f:
            f.write(history)
        
        print("✅ Записано в историю!")
    else:
        print(f"❌ Ошибка публикации анонса: {tg_result.get('description', 'Неизвестно')}")
else:
    error = result.get("error", "Неизвестная ошибка")
