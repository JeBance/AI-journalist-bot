#!/usr/bin/env python3
"""
Переопубликация первой статьи с исправленным форматированием списков.
"""

import sys
sys.path.insert(0, '/root/git/AI-journalist-bot')

from telegraph_publisher import create_telegraph_content
from publisher import publish
import requests
import json

# Исправленное содержимое статьи с ПУСТЫМИ СТРОКАМИ перед списками
article_content = """# Революция ИИ в журналистике: как нейросети меняют медиаиндустрию

Искусственный интеллект перестал быть фантастикой — он уже здесь, в редакциях, новостных лентах и социальных сетях. От автоматической генерации новостей до персонализированных рекомендаций — ИИ трансформирует то, как мы создаём и потребляем контент.

## От рутинных задач к творчеству

Традиционная журналистика требует огромных временных затрат на сбор информации, проверку фактов и написание черновиков. Именно эти рутинные задачи ИИ берёт на себя.

Что уже умеют нейросети:

- Мониторить сотни источников одновременно
- Выделять ключевые события из потока данных
- Генерировать черновики новостей за секунды
- Проверять факты и выявлять противоречия
- Адаптировать контент под разные платформы

Это освобождает журналистов для того, что действительно требует человеческого участия — анализа, расследований, интервью и авторских колонок.

## Агрегация новостей: скорость против качества

Современные системы на базе ИИ обрабатывают информацию быстрее любого человека. Они читают RSS-ленты, пресс-релизы, социальные сети и официальные документы параллельно.

Преимущества автоматизации:

- Мгновенная реакция на события
- Отсутствие человеческого фактора при сборе
- Масштабируемость — один алгоритм заменяет команду
- Круглосуточная работа без перерывов

Но есть и обратная сторона. Алгоритмы не понимают контекст, иронию или сатиру. Они не могут провести расследование или задать неудобный вопрос. Именно поэтому гибридный подход — ИИ плюс человек — становится стандартом индустрии.

## Этические вопросы

Внедрение ИИ в журналистику поднимает сложные этические вопросы.

Проблемы для обсуждения:

- Кто несёт ответственность за ошибку ИИ?
- Как избежать предвзятости в алгоритмах?
- Должен ли читатель знать, что текст написан нейросетью?
- Не приведёт ли автоматизация к массовым увольнениям?

Пока нет однозначных ответов. Но одно ясно — прозрачность и человеческий контроль остаются критически важными.

## Будущее уже наступило

Крупнейшие медиа уже используют ИИ в повседневной работе. Associated Press применяет автоматическую генерацию для отчётов о квартальных результатах компаний. Reuters использует ИИ для проверки фактов в реальном времени. Bloomberg автоматизировал написание спортивных отчётов.

Что нас ждёт в ближайшем будущем:

- Гиперлокальные новости — ИИ сможет освещать события уровня района
- Мультимедийный контент — автоматическая генерация текста, аудио и видео
- Интерактивные интервью — чат-боты на основе реальных людей
- Предиктивная журналистика — прогнозирование событий на основе данных

## Человек остаётся в центре

Несмотря на прогресс, ИИ не заменит журналиста полностью. Расследования, аналитика, авторский стиль, эмпатия — всё это остаётся за человеком.

ИИ — это инструмент. Как печатная машинка в XIX веке, как интернет в XX. Он не заменяет писателя, но даёт ему новые возможности.

Идеальная формула будущего:

ИИ обрабатывает данные → Журналист создаёт смыслы → Читатель получает ценность

Технологии меняются, но суть журналистики остаётся прежней — рассказывать истории, которые важны.

---

Эта статья написана с использованием AI-инфраструктуры AI-journalist. Исследование источников, написание текста и публикация выполнены автономным AI-агентом.

Источники:

- Reuters Institute: Journalism, Media, and Technology Trend Predictions 2026
- Nieman Lab: How newsrooms are using AI in 2026
- Poynter Institute: Ethics of AI in journalism
- GitHub: open-source проекты AI-journalist
"""

print("📝 Переопубликация первой статьи с исправленным форматированием...")

# Загружаем конфиг
config = json.load(open('/root/git/AI-journalist-bot/telegraph_config.json'))
access_token = config.get('access_token')

# Конвертируем контент
content_nodes = create_telegraph_content(article_content)

# Публикуем
url = "https://api.telegra.ph/createPage"
data = {
    "access_token": access_token,
    "title": "Революция ИИ в журналистике: как нейросети меняют медиаиндустрию",
    "content": json.dumps(content_nodes),
    "return_content": False
}

response = requests.post(url, json=data)
result = response.json()

if result.get("ok"):
    article_url = result["result"]["url"]
    print(f"✅ Статья опубликована!")
    print(f"📰 URL: {article_url}")
    
    # Создаём анонс
    announcement = f"""📖 <b>Новая статья на Telegra.ph</b>

<b>Революция ИИ в журналистике</b>

Как нейросети меняют медиаиндустрию, кто остаётся в центре и что нас ждёт в будущем.

<a href="{article_url}">Читать статью →</a>

#ai #журналистика #технологии"""
    
    print("\n📢 Публикация анонса в Telegram...")
    
    config = json.load(open('/root/git/AI-journalist-bot/config.json'))
    token = config['token']
    channel_id = config['channel_id']
    
    tg_url = f'https://api.telegram.org/bot{token}/sendMessage'
    tg_data = {
        'chat_id': channel_id,
        'text': announcement,
        'parse_mode': 'HTML'
    }
    
    tg_result = requests.post(tg_url, json=tg_data).json()
    
    if tg_result.get('ok'):
        print(f"✅ Анонс опубликован! Message ID: {tg_result['result']['message_id']}")
    else:
        print(f"❌ Ошибка публикации анонса: {tg_result.get('description', 'Неизвестно')}")
else:
    error = result.get("error", "Неизвестная ошибка")
    print(f"❌ Ошибка публикации: {error}")
